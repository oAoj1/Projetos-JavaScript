var fundo = window.document.getElementById('fundo')

function vermelho(){
    fundo.style.backgroundColor = 'red'
    fundo.style.color = 'white'
    fundo.style.transition = '0.3s'
}

function verde(){
    fundo.style.backgroundColor = 'green'
    fundo.style.color = 'white'
    fundo.style.transition = '0.3s'
}

function azul(){
    fundo.style.backgroundColor = 'blue'
    fundo.style.color = 'white'
    fundo.style.transition = '0.3s'
}
